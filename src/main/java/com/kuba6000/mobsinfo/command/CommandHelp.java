package com.kuba6000.mobsinfo.command;

import net.minecraft.command.CommandBase;
import net.minecraft.command.ICommandSender;
import net.minecraft.util.ChatComponentText;
import net.minecraft.util.StatCollector;

public class CommandHelp extends CommandBase {

    enum Translations {

        POSSIBLE_COMMANDS,
        USAGE,;

        final String key;

        Translations() {
            key = "mobsinfo.command.help." + this.name()
                .toLowerCase();
        }

        public String get() {
            return StatCollector.translateToLocal(key);
        }

        public String get(Object... args) {
            return StatCollector.translateToLocalFormatted(key, args);
        }

        public String getKey() {
            return key;
        }

        @Override
        public String toString() {
            return get();
        }
    }

    @Override
    public String getCommandName() {
        return "help";
    }

    @Override
    public String getCommandUsage(ICommandSender p_71518_1_) {
        return "help " + Translations.USAGE.get();
    }

    @Override
    public boolean canCommandSenderUseCommand(ICommandSender p_71519_1_) {
        return true;
    }

    @Override
    public void processCommand(ICommandSender p_71515_1_, String[] p_71515_2_) {
        p_71515_1_.addChatMessage(new ChatComponentText(Translations.POSSIBLE_COMMANDS.get()));
        CommandHandler.commands.values()
            .forEach(
                c -> p_71515_1_.addChatMessage(new ChatComponentText("/mobsinfo " + c.getCommandUsage(p_71515_1_))));
    }
}
